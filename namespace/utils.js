const Utils = {
  Math: {
    sum: function (num1, num2) {
      return num1 + num2;
    },
    minus: function (num1, num2) {
      return num1 - num2;
    },
    multiply: function (num1, num2) {
      return num1 * num2;
    },
    divide: function (num1, num2) {
      return num1 / num2;
    },
  },
};
